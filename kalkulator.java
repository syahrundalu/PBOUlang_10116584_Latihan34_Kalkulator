public class kalkulator {
    public double value1, value2;

public double tambahBilangan()    {
    return value1 + value2;
}
public double kurangBilangan()    {
    return value1 - value2;
}
public double kaliBilangan()    {
    return value1 * value2;
}
public double bagiBilangan()    {
    return value1 / value2;
}
}
